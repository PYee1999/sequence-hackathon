public class Card {

    private int cardID;
    private String cardSuitNum;

    public int getCardID() {
        return cardID;
    }

    public void setCardID(int cardID) {
        this.cardID = cardID;
    }

    public String getCardSuitNum() {
        return cardSuitNum;
    }

    public void setCardSuitNum(String cardSuitNum) {
        this.cardSuitNum = cardSuitNum;
    }

}
